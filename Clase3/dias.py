'''
dias de la semana

'''

dias=int(input("ingrese un numero del 1 al 7:"))

if dias==1:
    print("Lunes")  
elif dias==2:
    print("Martes")
elif dias==3:
    print("Miercoles")
elif dias==4:
    print("Jueves")
elif dias==5:
    print("Viernes")
elif dias==6:
    print("Sabado")
elif dias==7:
    print("Domingo")
else:
    print("Numero incorrecto")